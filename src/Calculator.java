public class Calculator {
    //ma posiadac 5 metod odpowiedzialnych za wyniki matematyczne
    public int dodawanie(int number, int number2) {
       return number + number2;
    }
    public int odejmowanie(int number, int number2) {
        return number - number2;
    }
    public int mnozenie(int number, int number2) {
        return number * number2;
    }
    public int dzielenie(int number, int number2) {
        return number / number2;
    }
    public int modulo(int number, int number2) {
        return number % number2;
    }
}
